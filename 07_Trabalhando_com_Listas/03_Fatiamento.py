# Fatiamento

# Fatiamento é uma técnica que permite acessar partes específicas de uma lista, string ou outra estrutura de dados sequencial.

lista = ["p", "y", "t", "h", "o", "n"]

print(lista[2:])
print(lista[:2])
print(lista[1:3])
print(lista[0:3:2])
print(lista[::])
print(lista[::-1]) # inverte a lista