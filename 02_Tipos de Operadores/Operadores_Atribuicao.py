# Atribuição com divisão
Saldo1 = 550
Saldo1 /= 2
print(Saldo1)


# Atribuição com Modulo
Saldo2 = 550
Saldo2 %= 280
print(Saldo2)


# Atribuição com exponenciação
Saldo3 = 80
Saldo3 **= 2
print(Saldo3)

